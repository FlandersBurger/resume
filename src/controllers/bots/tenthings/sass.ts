import { IGame } from "@models/tenthings/game";
import httpClient from "@root/http-client";
import { Sass } from "@models/index";

export default async (game: IGame, text: string) => {
  if (game.settings.sass && game.enabled) {
    const sassText = await sass(text);
    if (sassText) {
      if (sassText.includes("http") && !sassText.includes("paypal")) {
        game.provider.sendMedia(game, sassText);
      } else {
        game.provider.message(game, sassText);
      }
    }
  }
};

const checkString = (text: string, str: string = ""): boolean =>
  text
    .toLowerCase()
    .replace(/[^\w\s]/gi, "")
    .split(" ")
    .includes(str);

const getJoke = async () => {
  const response = await httpClient().get("https://icanhazdadjoke.com/", {
    headers: {
      Accept: "application/json",
    },
  });
  return response.data.joke;
};

const matchesTrigger = (text: string, trigger: string): boolean =>
  trigger
    .toLowerCase()
    .replace(/[^\w\s]/gi, "")
    .split(" ")
    .every((word) => checkString(text, word));

const sass = async (text: string): Promise<string | undefined> => {
  const botMentioned = checkString(text, "bot");

  if (botMentioned) {
    if (checkString(text, "joke") || checkString(text, "funny")) return await getJoke();
    if (checkString(text, "recursion")) return text;
  } else if (/^[^\s]+ \bis\b$/.test(text)) {
    const adjective = adjectives[Math.floor(Math.random() * adjectives.length)];
    return `${text} ${adjective}!`;
  }

  const entries = await Sass.find({ enabled: true, requiresBotMention: botMentioned }).sort({ order: 1 }).lean();
  const matched = entries.find(
    ({ triggers }) => triggers.length === 0 || triggers.some((trigger) => matchesTrigger(text, trigger)),
  );
  if (!matched || matched.responses.length === 0) return;
  return matched.responses[Math.floor(Math.random() * matched.responses.length)];
};

